CREATE VIEW dbo.cts_cuentas_salesforce_v
AS
SELECT     cl.attribute1 AS CORPORATIVO, CL.CUSTOMER_NAME AS CUENTA_PRINCIPAL, CASE WHEN CAST(CL.ANUAL_SALES AS decimal(18, 2)) 
                      > 200000 THEN 'GRANDE' WHEN CAST(CL.ANUAL_SALES AS decimal(18, 2)) BETWEEN 600001 AND 2000000 THEN 'MEDIANA' ELSE 'CHICA' END AS tipo, 
                      CL.PHONE AS TELEFONO, CL.RESOURCE_NAME AS VENDEDOR_CONTACTO, 'CLIENTE' AS TIPO_CUENTA, CL.RFC, cl.location AS DK,
                          (SELECT     TOP (1) Star1Name
                            FROM          dbo.CatStarsFirstLevel AS CF
                            WHERE      (DK IS NOT NULL) AND (IsNew = 1) AND (DK = cl.location)) AS estrella_primer_nivel, CL.ANUAL_SALES AS INGRESOS_ANUALES, CL.DIAS_CREDITO, 
                      cl.direccion AS direccion_facturacion, cl.origen, cl.destino
FROM         CORPD..APPS.CTS_CTES_SALESFORCE_V AS CL

